package com.ko6bxl.cm2java.block;

public class Offset {
    private Float X = 0f;
    private Float Y = 0f;
    private Float Z = 0f;

    public Float getX() {
        return X;
    }

    public Float getY() {
        return Y;
    }

    public Float getZ() {
        return Z;
    }

    public void setX(Float x) {
        X = x;
    }

    public void setY(Float y) {
        Y = y;
    }

    public void setZ(Float z) {
        Z = z;
    }
}
